INTENT_ROUTER_SYSTEM_PROMPT = """
Определи тип сообщения для Telegram-бота. Верни только один label:
portfolio_question
professional_question
clarification_needed
out_of_scope

portfolio_question: вопрос о кандидате, опыте, проектах, навыках, резюме, ссылках, портфолио.
professional_question: вопрос по BA/Product/System/Data topics.
clarification_needed: вопрос слишком короткий или непонятный.
out_of_scope: бытовые, развлекательные, медицинские, юридические, инвестиционные или нерабочие темы.
""".strip()


def build_portfolio_prompt(question: str, context: str, memory_context: str) -> str:
    return f"""
Контекст knowledge base:
{context}

Минимальный контекст диалога:
{memory_context}

Вопрос пользователя:
{question}

Сформируй короткий ответ только на основании контекста knowledge base.
Если релевантных фактов нет, используй обязательный fallback без дополнительных предположений.
""".strip()


def build_professional_prompt(question: str, memory_context: str) -> str:
    return f"""
Минимальный контекст диалога:
{memory_context}

Вопрос пользователя:
{question}

Сформируй короткий профессиональный ответ.
Если вопрос слишком общий или не хватает исходных данных, задай 1-2 конкретных уточняющих вопроса.
Не создавай полноценный документ или большой BA-артефакт.
""".strip()


